		<h2> 1.	Basic Research and Estimate	</h2>
		<div class="answer">
			<p class =textbody>As a first step we ask you some basic questions that allow us to know your needs and expectations. First we need to know if your site will be static or dynamic, how many pages your site will have, and what material is already available. We disucss your preliminary ideas regarding the organization and structure of the contents of the website, labeling and formatting preferences . What does your site need to accomplish? What tools do you need? Which information and material to be presented is most important? With this information at hand we are able to provide you with an estimate and an anticipated delivery date.</p>
		</div>
		<br>
		<br>
		<h2> 2.	 Overview of the Site and Interview</h2>
		<div class="answer">
			<p class =textbody>Next, We develop a basic document with general structure of organization of information. We decide the location of elements on the screen such as menus, links, images and text, tools, forms,  or maps. This phase ends when we meed with you and decide on one of the three preliminary design templates for your site. Upon choosing one you may suggest modifications to the template to better meet your needs.</p>
		</div>
		<br>
		<br>
		<h2> 3.	Ongoing Communication </h2>
		<div class="answer">
			<p class =textbody>Through the process of development we further inquire if the website adapts to the targeted audience. Is the design internally consistent, and does it reflect desired standards? Is there irrelevant or excessive information that needs to be removed, missing information that needs to be added? Is navigation through the website intuitive and easily remembered by visitors? Does the website reflect industry website standard conventions and norms? Midway through the project we meet again and show you the progress the website is making. Changes to be included in the final version are incorporated at this stage.</p>
		</div>
		<br>
		<br>
		<h2> 4.	Testing</h2>
		<div class="answer">
